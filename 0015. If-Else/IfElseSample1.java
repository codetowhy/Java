public class IfElseSample1 {
    public static void main(String[] args)
    {
      int a = 10;
      int b = 20;

      if(a > b){
        System.out.println("Variable a is greater than b");
      }
      else if(b>a){
        System.out.println("Variable b is greater than a");
      }
      else{
        System.out.println("Variable a equal to b");
      }
    }
}
